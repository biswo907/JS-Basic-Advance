// 1. Number Literals
let integerLiteral = 42;          // Integer literal
let floatLiteral = 3.14;          // Floating-point literal
let hexLiteral = 0xFF;            // Hexadecimal literal (255 in decimal)
let binaryLiteral = 0b1010;       // Binary literal (10 in decimal)
let octalLiteral = 0o12;          // Octal literal (10 in decimal)

// 2. String Literals
let singleQuoteLiteral = 'Hello, World!';  // Single-quote string literal
let doubleQuoteLiteral = "Hello, World!";  // Double-quote string literal
let templateLiteral = `Hello, ${name}!`;   // Template literal with interpolation

// 3. Boolean Literals
let isTrue = true;    // Boolean literal true
let isFalse = false;  // Boolean literal false

// 4. Object Literals
let person = {
    name: 'John',
    age: 30,
    isEmployed: true
}; // Object literal with key-value pairs

// 5. Array Literals
let numbers = [1, 2, 3, 4, 5];            // Array of numbers
let mixedArray = [1, 'two', true, null];  // Array with mixed types

// 6. Null Literal
let emptyValue = null;  // Null literal representing no value

// 7. Undefined Literal
let unassigned;  // Undefined literal (implicitly)

// 8. Regular Expression Literals
let regex = /ab+c/;  // Regular expression literal for matching patterns

// 9. Template Literals
let greeting = `Hello, ${name}!`; // Template literal with interpolation
let multiLine = `This is line 1
This is line 2`;  // Multi-line string using template literal
